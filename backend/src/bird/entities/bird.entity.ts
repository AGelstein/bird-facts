export class Bird {
    id: string;
    name: string;
    color: string;
    breed: string;
    age: number;

    constructor(id: string, name: string, color: string, breed: string, age: number) {
        this.id = id;
        this.name = name;
        this.color = color;
        this.breed = breed;
        this.age = age;
    }
}
